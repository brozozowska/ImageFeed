import UIKit

final class SplashViewController: UIViewController {
    
    // MARK: - Private Properties
    private let storage = OAuth2TokenStorage()
    private let splashViewImageName = "Vector"
    private let profileService = ProfileService.shared
    private let profileImageService = ProfileImageService.shared
    
    // MARK: - UI Elements
    private lazy var splashViewImage: UIImageView = {
        let imageView = UIImageView()
        imageView.image = UIImage(named: splashViewImageName)
        return imageView
    }()
    
    // MARK: - Lifecycle
    override func viewDidLoad() {
        super.viewDidLoad()
        view.backgroundColor = .ypBlack
        addSubviews()
        setupLayout()
    }
    
    override func viewDidAppear(_ animated: Bool) {
        super.viewDidAppear(animated)
        if let token = storage.token {
            print("üîë [SplashViewController.viewDidAppear]: –¢–æ–∫–µ–Ω –Ω–∞–π–¥–µ–Ω: –ø–æ–ª—É—á–∞–µ–º –ø—Ä–æ—Ñ–∏–ª—å –∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —ç–∫—Ä–∞–Ω—É —Å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è–º–∏")
            fetchProfile(token: token)
        } else {
            print("üîë [SplashViewController.viewDidAppear]: –¢–æ–∫–µ–Ω–∞ –Ω–µ—Ç: –ø–µ—Ä–µ—Ö–æ–¥ –∫ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏")
            presentAuthViewController()
        }
    }
    
    // MARK: - Setup Methods
    private func addSubviews() {
        [
            splashViewImage
        ].forEach {
            $0.translatesAutoresizingMaskIntoConstraints = false
            view.addSubview($0)
        }
    }
    
    private func setupLayout() {
        NSLayoutConstraint.activate([
            splashViewImage.centerXAnchor.constraint(equalTo: view.centerXAnchor),
            splashViewImage.centerYAnchor.constraint(equalTo: view.centerYAnchor),
        ])
    }
    
    // MARK: - Private Methods
    private func presentAuthViewController() {
        let storyboard = UIStoryboard(name: "Main", bundle: .main)
        guard let authViewController = storyboard.instantiateViewController(
            withIdentifier: "AuthViewController"
        ) as? AuthViewController else {
            assertionFailure("‚ùå [SplashViewController.presentAuthViewController]: –ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ AuthViewController –ø–æ ID")
            return
        }
        authViewController.delegate = self
        authViewController.modalPresentationStyle = .fullScreen
        present(authViewController, animated: true)
    }
    
    private func switchToTabBarController() {
        guard let windowScene = UIApplication.shared.connectedScenes.first as? UIWindowScene,
            let window = windowScene.windows.first else {
            assertionFailure("‚ùå [SplashViewController.switchToTabBarController]: –ù–µ—Ç –∞–∫—Ç–∏–≤–Ω–æ–π UIWindowScene")
            return
        }
        let storyboard = UIStoryboard(name: "Main", bundle: .main)
        let tabBarController = storyboard.instantiateViewController(
            withIdentifier: "TabBarController"
        )
        window.rootViewController = tabBarController
        window.makeKeyAndVisible()
    }
}

// MARK: - AuthViewControllerDelegate
extension SplashViewController: AuthViewControllerDelegate {
    
    func didAuthenticate(_ vc: AuthViewController) {
        vc.dismiss(animated: true) { [weak self] in
            self?.switchToTabBarController()
        }
        guard let token = storage.token else { return }
        fetchProfile(token: token)
    }
    
    func fetchProfile(token: String) {
        UIBlockingProgressHUD.show()
        profileService.fetchProfile(token: token) { [weak self] result in
            UIBlockingProgressHUD.dismiss()
            guard let self else { return }
            switch result {
            case .success(let profile):
                let username = profile.username
                profileImageService.fetchProfileImageURL(username: username, token: token) { _ in
                    print("‚úÖ [SplashViewController.fetchProfile]: –í—ã–∑–æ–≤ –º–µ—Ç–æ–¥–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –∞–≤–∞—Ç–∞—Ä–∞")
                }
                self.switchToTabBarController()
            case .failure:
                // TODO: [Sprint 11] –ü–æ–∫–∞–∂–∏—Ç–µ –æ—à–∏–±–∫—É –ø–æ–ª—É—á–µ–Ω–∏—è –ø—Ä–æ—Ñ–∏–ª—è
                break
            }
        }
    }
}
